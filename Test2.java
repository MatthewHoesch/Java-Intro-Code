import java.util.Scanner;
public class Test2
{
  public static void main (String[] args)
  {
    double table[][] = new double[3][5];
    int row, col;
    Scanner kb= new Scanner (System.in);
    System.out.println("Enter in 15 numbers and store in 3 rows and 5 cols");
    for(row=0;row<3;row++)
      for(col=0; col<5;col++)
      table[row][col]=kb.nextDouble();
    //Print table
    
    for(row=0;row<3;row++)
    {
      for(col=0;col<5;col++)
        System.out.print(table[row][col] + "  ");
      System.out.println();
    }
  }
}